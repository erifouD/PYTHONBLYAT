# Инициализация данных о фруктах на складе
fruit_inventory = {
    'Абрикос': {'Склад': 1, 'Вес': 30, 'Цена за 1 кг': 120},
    'Авокадо': {'Склад': 1, 'Вес': 19, 'Цена за 1 кг': 90},
    'Алыча': {'Склад': 1, 'Вес': 32, 'Цена за 1 кг': 67},
    'Апельсин': {'Склад': 1, 'Вес': 67, 'Цена за 1 кг': 129},
    'Арбуз': {'Склад': 1, 'Вес': 129, 'Цена за 1 кг': 19},
    'Гранат': {'Склад': 2, 'Вес': 36, 'Цена за 1 кг': 56},
    'Грейпфрут': {'Склад': 2, 'Вес': 23, 'Цена за 1 кг': 78},
    'Груша': {'Склад': 2, 'Вес': 57, 'Цена за 1 кг': 20},
    'Дыня': {'Склад': 2, 'Вес': 48, 'Цена за 1 кг': 45},
    'Инжир': {'Склад': 2, 'Вес': 12, 'Цена за 1 кг': 54},
    'Кешью': {'Склад': 2, 'Вес': 31, 'Цена за 1 кг': 73},
}
# Инициализация товаров в магазине
store_items = {
    'Апельсин': {'В наличии': 12, 'Необходимо': 20},
    'Груша': {'В наличии': 3, 'Необходимо': 10},
    'Арбуз': {'В наличии': 23, 'Необходимо': 35},
    'Авокадо': {'В наличии': 2, 'Необходимо': 8},
    'Дыня': {'В наличии': 0, 'Необходимо': 14},
}
# Заявки на каждый склад
warehouse_orders = {}
# Оформление заявок
for fruit, data in fruit_inventory.items():
    if fruit in store_items:
        in_stock = store_items[fruit]['В наличии']
        needed = store_items[fruit]['Необходимо']
        # Рассчитываем вес заказа
        order_quantity = min(needed, in_stock, 30)
        if order_quantity > 0:
            if data['Склад'] not in warehouse_orders:
                warehouse_orders[data['Склад']] = {'Фрукты': [], 'Вес': 0}
            # Добавляем информацию о заявке
            warehouse_orders[data['Склад']]['Фрукты'].append({'Название': fruit, 'Количество': order_quantity})
            warehouse_orders[data['Склад']]['Вес'] += order_quantity
            # Обновляем данные в магазине
            store_items[fruit]['Необходимо'] -= order_quantity
            store_items[fruit]['В наличии'] -= order_quantity
# Вывод результата в консоль
for warehouse, order_data in warehouse_orders.items():
    print(f"\nЗаявка для склада {warehouse}:")
    print(f"Вес заявки: {order_data['Вес']} кг")
    print("Список фруктов:")
    for fruit_info in order_data['Фрукты']:
        print(f"{fruit_info['Название']} - {fruit_info['Количество']} кг")
